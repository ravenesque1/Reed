import SwiftUI

struct ReedHiddenNavBarView<Content>: View where Content: View {
    private let content: Content

    public init(@ViewBuilder content: () -> Content) {
        self.content = content()
    }

    var body : some View {
        ZStack {
            content
                .navigationBarTitle("")
                .navigationBarHidden(true)
        }
    }
}
